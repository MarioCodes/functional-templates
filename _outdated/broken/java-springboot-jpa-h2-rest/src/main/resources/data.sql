DROP TABLE IF EXISTS PRICES;

CREATE TABLE PRICES (
    PRICE_LIST INT AUTO_INCREMENT PRIMARY KEY,
    BRAND_ID INT,
    START_DATE SMALLDATETIME NOT NULL,
    END_DATE SMALLDATETIME NOT NULL,
    PRODUCT_ID INT,
    PRIORITY INT,
    PRICE VARCHAR(255),
    CURR VARCHAR(50)
);

